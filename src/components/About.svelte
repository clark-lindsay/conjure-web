<div class="overflow-y-auto pb-20">
  <h2 class="text-blue-600 text-2xl mx-2">About</h2>
  <div class="mx-2">
    This application was written to handle some of the odd ruling and mechanics
    of conjuration spells that summon multiple creatures in the 5th edition of
    Dungeons and Dragons.
  </div>
  <details class="ml-4">
    <summary class="text-blue-600 hover:text-blue-800 text-lg m-2">
      Why does it work the way it does?
    </summary>
    <div class="ml-8 mr-2">
      The method used here was derived from this <a
        class="text-blue-400"
        href="https://media.wizards.com/2015/downloads/dnd/SA_Compendium_1.02.pdf">Sage
        Advice Compendium</a>. Some minor tweaks were made to make an automated
      method of summoning creatures more viable for more people.
    </div>
  </details>
  <details class="ml-4">
    <summary class="text-blue-600 hover:text-blue-800 text-lg m-2">
      How do the parameters work?
    </summary>
    <ul class="list-disc ml-8 mr-2">
      <li>
        On desktop, you should see the parameters on the left side of your
        screen, and on mobile they can be accessed by the "hamburger" menu at
        the top left of the screen (which is not visible while you are reading
        this).
      </li>
      <li>
        The sourcebooks that you select determine what creatures can be
        generated, so you should only select sourcebooks that you have access
        to, or that pertain to the setting of your game.
      </li>
      <li>
        The terrain settings determine what terrains the summoned creatures will
        be able to traverse. Selecting "Land" will add creatures with a walking
        speed to the pool of possibilities, and similarly "Water" will add
        creatures with a swimming speed, and "Air" will add creatures with a
        flying speed.
      </li>
      <li>
        Lastly, the challenge rating determines the challenge rating of the
        summoned creatures, as well as the number of creatures summoned (as
        detailed by the spell descriptions).
      </li>
    </ul>
  </details>
